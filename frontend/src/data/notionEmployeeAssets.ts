const normalizeEmployeeNo = (value?: string | null) =>
  (value || '').trim().toUpperCase();

const normalizeName = (value?: string | null) =>
  (value || '')
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\[[^\]]*\]/g, ' ')
    .replace(/[^a-zA-Z0-9]+/g, ' ')
    .trim()
    .toLowerCase()
    .replace(/\s+/g, ' ');

const nameTokens = (value?: string | null) =>
  normalizeName(value)
    .split(' ')
    .filter((token) => token.length > 1);

const employeeFullName = (employee: Pick<Employee, 'full_name' | 'first_name' | 'middle_name' | 'last_name'>) =>
  employee.full_name || [employee.first_name, employee.middle_name, employee.last_name].filter(Boolean).join(' ');

const assetFullName = (asset: NotionEmployeeAsset) =>
  asset.fullName || [asset.firstName, asset.lastName].filter(Boolean).join(' ');

const byEmployeeNo = new Map(
  notionEmployeeAssets
    .filter((asset) => asset.employeeNo)
    .map((asset) => [normalizeEmployeeNo(asset.employeeNo), asset])
);

export const findNotionEmployeeAsset = (
  employee: Pick<Employee, 'employee_no' | 'full_name' | 'first_name' | 'middle_name' | 'last_name'>
) => {
  const employeeNoMatch = byEmployeeNo.get(normalizeEmployeeNo(employee.employee_no));
  if (employeeNoMatch) return employeeNoMatch;

  const employeeName = normalizeName(employeeFullName(employee));
  if (!employeeName) return undefined;

  const exactNameMatch = notionEmployeeAssets.find((asset) => normalizeName(assetFullName(asset)) === employeeName);
  if (exactNameMatch) return exactNameMatch;

  const employeeTokens = nameTokens(employeeFullName(employee));
  const employeeFirst = nameTokens(employee.first_name)[0] || employeeTokens[0];
  const employeeLast = nameTokens(employee.last_name).slice(-1)[0] || employeeTokens.slice(-1)[0];
  if (!employeeFirst || !employeeLast) return undefined;

  return notionEmployeeAssets.find((asset) => {
    const assetTokens = nameTokens(assetFullName(asset));
    const assetFirst = nameTokens(asset.firstName)[0] || assetTokens[0];
    const assetLast = nameTokens(asset.lastName).slice(-1)[0] || assetTokens.slice(-1)[0];

    if (!assetFirst || !assetLast) return false;
    if (employeeFirst === assetFirst && employeeLast === assetLast) return true;

    const hasEmployeeEndpoints = assetTokens.includes(employeeFirst) && assetTokens.includes(employeeLast);
    const hasAssetEndpoints = employeeTokens.includes(assetFirst) && employeeTokens.includes(assetLast);
    return hasEmployeeEndpoints || hasAssetEndpoints;
  });
};

export const getNotionEmployeeBirthday = (
  employee: Pick<Employee, 'employee_no' | 'full_name' | 'first_name' | 'middle_name' | 'last_name' | 'birth_date'>
) => employee.birth_date || findNotionEmployeeAsset(employee)?.birthday || '';

export const getNotionEmployeePhotoUrl = (
  employee: Pick<Employee, 'employee_no' | 'full_name' | 'first_name' | 'middle_name' | 'last_name'>
) => findNotionEmployeeAsset(employee)?.photoUrl || '';
